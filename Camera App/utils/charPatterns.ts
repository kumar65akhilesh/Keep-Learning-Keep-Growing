/** Grid dimensions for the reference patterns */
export const PATTERN_COLS = 5;
export const PATTERN_ROWS = 7;

/**
 * Get the set of expected grid cells (col, row) for a character.
 * Returns null if the character has no reference pattern.
 */
export function getExpectedCells(char: string): Set<string> | null {
  const pattern = CHAR_PATTERNS[char.toUpperCase()];
  if (!pattern) return null;

  const cells = new Set<string>();
  for (let row = 0; row < PATTERN_ROWS; row++) {
    for (let col = 0; col < PATTERN_COLS; col++) {
      if (pattern[row]?.[col] === '#') {
        cells.add(`${col},${row}`);
      }
    }
  }
  return cells;
}
